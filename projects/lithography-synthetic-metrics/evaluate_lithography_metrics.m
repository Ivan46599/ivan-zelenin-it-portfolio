clear;                           
clc;                             
close all;                       

rng(7);                          % Фиксирует генератор случайных чисел для воспроизводимости результата.

% -----------------------------
% 1. Параметры синтетической структуры фоторезиста
% -----------------------------
pixelSize = 1.0;                 % Задает шаг сетки по координатам x и y.
xSize = 240;                     % Задает размер расчетной области по x.
ySize = 500;                     % Задает размер расчетной области по y.
nominalCD = 45;                  % Задает номинальную ширину линии, то есть расчетный CD.
lineCenter = 120;                % Задает координату центра линии по x.
resistHeight = 80;               % Задает максимальную высоту проявленного фоторезиста.
lerSigma = 1.5;                  % Задает среднеквадратическую шероховатость края.
correlationLength = 10;          % Задает длину корреляции шероховатости вдоль линии.
edgeCorrelation = 0.25;          % Задает степень корреляции между левым и правым краем.
sidewallBlur = 2.5;              % Задает сглаженность боковой стенки линии.
heightNoiseSigma = 1.2;          % Задает стандартное отклонение шума карты высоты.
thresholdFraction = 0.5;         % Задает относительный порог выделения линии по высоте.

x = 0:pixelSize:xSize;           % Формирует одномерную сетку координаты x.
y = 0:pixelSize:ySize;           % Формирует одномерную сетку координаты y.
[xGrid, yGrid] = meshgrid(x, y); % Создает двумерные координатные сетки для карты высоты.
Ny = length(y);                  % Определяет количество строк в карте высоты.

% -----------------------------
% 2. Генерация коррелированной шероховатости краев
% -----------------------------
sigmaPx = correlationLength / pixelSize; % Переводит длину корреляции из нанометров в пиксели.
radius = ceil(4 * sigmaPx);              % Задает радиус гауссова ядра сглаживания.
k = -radius:radius;                      % Формирует координаты дискретного ядра сглаживания.
g = exp(-0.5 * (k / sigmaPx).^2);        % Вычисляет значения гауссовой функции.
g = g / sum(g);                          % Нормирует ядро так, чтобы сумма коэффициентов была равна единице.

common = conv(randn(1, Ny), g, 'same');  % Создает общую коррелированную составляющую шероховатости.
leftInd = conv(randn(1, Ny), g, 'same'); % Создает независимую составляющую шероховатости левого края.
rightInd = conv(randn(1, Ny), g, 'same');% Создает независимую составляющую шероховатости правого края.
common = common / std(common);           % Нормирует общую составляющую на единичное стандартное отклонение.
leftInd = leftInd / std(leftInd);        % Нормирует левую независимую составляющую.
rightInd = rightInd / std(rightInd);     % Нормирует правую независимую составляющую.

rho = edgeCorrelation;                                                    % Сохраняет коэффициент корреляции в короткую переменную.
leftRough = lerSigma * (sqrt(rho) * common + sqrt(1 - rho) * leftInd);    % Формирует шероховатость левого края с заданной sigma.
rightRough = lerSigma * (sqrt(rho) * common + sqrt(1 - rho) * rightInd);  % Формирует шероховатость правого края с заданной sigma.

nominalLeft = lineCenter - nominalCD / 2; % Вычисляет номинальное положение левого края линии.
nominalRight = lineCenter + nominalCD / 2;% Вычисляет номинальное положение правого края линии.
leftTrue = nominalLeft + leftRough;       % Добавляет шероховатость к левому краю.
rightTrue = nominalRight + rightRough;    % Добавляет шероховатость к правому краю.

% -----------------------------
% 3. Формирование карты высоты фоторезиста
% -----------------------------
leftTransition = 1 ./ (1 + exp(-(xGrid - leftTrue') / sidewallBlur));  % Формирует плавный переход высоты на левом краю.
rightTransition = 1 ./ (1 + exp((xGrid - rightTrue') / sidewallBlur));  % Формирует плавный переход высоты на правом краю.
heightMap = resistHeight .* leftTransition .* rightTransition;          % Получает двумерную карту высоты линии.
heightMap = heightMap + heightNoiseSigma * randn(size(heightMap));      % Добавляет случайный шум измерения высоты.
heightMap(heightMap < 0) = 0;                                           % Убирает отрицательные высоты как физически невозможные.

% -----------------------------
% 4. Выделение левого и правого края по порогу высоты
% -----------------------------
threshold = thresholdFraction * max(heightMap(:)); % Вычисляет абсолютный порог высоты.
leftEdges = [];                                    % Создает массив найденных координат левого края.
rightEdges = [];                                   % Создает массив найденных координат правого края.
yValid = [];                                       % Создает массив координат y для строк с найденными краями.

for row = 1:size(heightMap, 1)                     % Запускает цикл по всем строкам карты высоты.
    profile = heightMap(row, :);                   % Извлекает одномерный профиль высоты для текущей строки.
    idx = find(profile >= threshold);              % Находит индексы точек, высота которых выше порога.
    if isempty(idx)                                % Проверяет, есть ли в строке точки линии.
        continue;                                  % Пропускает строку, если структура не найдена.
    end                                           % Завершает проверку пустого набора индексов.

    iL = idx(1);                                   % Берет первый индекс выше порога как грубый левый край.
    iR = idx(end);                                 % Берет последний индекс выше порога как грубый правый край.
    if iR <= iL                                    % Проверяет, что найденная область имеет положительную ширину.
        continue;                                  
    end                                           

    if iL > 1                                      % Проверяет, можно ли уточнить левый край интерполяцией.
        x0 = x(iL - 1);                            % Берет координату точки ниже порога слева от края.
        x1 = x(iL);                                % Берет координату первой точки выше порога.
        v0 = profile(iL - 1);                      % Берет высоту точки ниже порога.
        v1 = profile(iL);                          % Берет высоту точки выше порога.
        leftX = x0 + (threshold - v0) * (x1 - x0) / (v1 - v0); % Интерполирует координату пересечения с порогом.
    else                                           % Обрабатывает случай, когда левый край находится на границе области.
        leftX = x(iL);                             % Использует координату первой точки выше порога без интерполяции.
    end                                           

    if iR < length(x)                              % Проверяет, можно ли уточнить правый край интерполяцией.
        x0 = x(iR + 1);                            % Берет координату точки ниже порога справа от края.
        x1 = x(iR);                                % Берет координату последней точки выше порога.
        v0 = profile(iR + 1);                      % Берет высоту точки ниже порога справа.
        v1 = profile(iR);                          % Берет высоту точки выше порога.
        rightX = x0 + (threshold - v0) * (x1 - x0) / (v1 - v0); % Интерполирует координату правого края.
    else                                           % Обрабатывает случай, когда правый край находится на границе области.
        rightX = x(iR);                            % Использует координату последней точки выше порога без интерполяции.
    end                                           

    if (rightX - leftX) > 10                       % Проверяет, что найденная ширина не является шумовым выбросом.
        leftEdges(end + 1) = leftX; %#ok<SAGROW>   % Добавляет координату левого края в массив результатов.
        rightEdges(end + 1) = rightX; %#ok<SAGROW> % Добавляет координату правого края в массив результатов.
        yValid(end + 1) = y(row); %#ok<SAGROW>     % Добавляет координату y текущей строки.
    end                                           
end                                               

% -----------------------------
% 5. Расчет CD и LWR по локальной ширине линии
% -----------------------------
widths = rightEdges - leftEdges;                  % Вычисляет локальную ширину линии в каждой строке.
CD = mean(widths);                                % Рассчитывает CD как среднее значение локальной ширины.
LWR_sigma = std(widths);                          % Рассчитывает LWR как стандартное отклонение ширины.
LWR_3sigma = 3 * LWR_sigma;                       % Переводит LWR в распространенную форму 3 sigma.

% -----------------------------
% 6. Расчет LER после удаления линейного тренда края
% -----------------------------
pLeft = polyfit(yValid, leftEdges, 1);            % Аппроксимирует левый край прямой линией.
pRight = polyfit(yValid, rightEdges, 1);          % Аппроксимирует правый край прямой линией.
leftResidual = leftEdges - polyval(pLeft, yValid);% Вычисляет отклонения левого края от линейного тренда.
rightResidual = rightEdges - polyval(pRight, yValid); % Вычисляет отклонения правого края от линейного тренда.
LER_left_sigma = std(leftResidual);               % Рассчитывает LER левого края как стандартное отклонение остатков.
LER_right_sigma = std(rightResidual);             % Рассчитывает LER правого края как стандартное отклонение остатков.
LER_avg_sigma = 0.5 * (LER_left_sigma + LER_right_sigma); % Усредняет LER по двум краям.
LER_avg_3sigma = 3 * LER_avg_sigma;               % Переводит среднюю LER в форму 3 sigma.

% -----------------------------
% 7. Демонстрационный расчет NILS на модельном интенсивностном профиле
% -----------------------------
dxI = 0.2;                                        % Задает шаг сетки интенсивностного профиля.
xI = 0:dxI:xSize;                                 % Формирует координаты профиля интенсивности.
intensityMin = 0.18;                              % Задает минимальную нормированную интенсивность внутри линии.
intensityMax = 1.0;                               % Задает максимальную нормированную интенсивность вне линии.
transitionLength = 5.0;                           % Задает характерную длину перехода интенсивности.
insideLeft = 1 ./ (1 + exp(-(xI - nominalLeft) / transitionLength)); % Формирует левый переход интенсивности.
insideRight = 1 ./ (1 + exp((xI - nominalRight) / transitionLength)); % Формирует правый переход интенсивности.
inside = insideLeft .* insideRight;               % Получает маску внутренней области линии.
intensity = intensityMax - (intensityMax - intensityMin) .* inside; % Формирует итоговый профиль интенсивности.
logI = log(max(intensity, 1e-12));                % Вычисляет логарифм интенсивности с защитой от log(0).
dLogIdx = gradient(logI, dxI);                    % Численно вычисляет производную d(ln I) / dx.
[~, leftIdx] = min(abs(xI - nominalLeft));        % Находит индекс точки, ближайшей к левому краю.
[~, rightIdx] = min(abs(xI - nominalRight));      % Находит индекс точки, ближайшей к правому краю.
NILS_left = nominalCD * abs(dLogIdx(leftIdx));    % Рассчитывает NILS на левом краю.
NILS_right = nominalCD * abs(dLogIdx(rightIdx));  % Рассчитывает NILS на правом краю.
NILS_avg = 0.5 * (NILS_left + NILS_right);        % Рассчитывает средний NILS по двум краям.

% -----------------------------
% 8. Вывод численных результатов в командное окно
% -----------------------------
fprintf('CD_mean_nm = %.3f\n', CD);               % Печатает средний CD.
fprintf('LWR_sigma_nm = %.3f\n', LWR_sigma);      % Печатает LWR в форме sigma.
fprintf('LWR_3sigma_nm = %.3f\n', LWR_3sigma);    % Печатает LWR в форме 3 sigma.
fprintf('LER_left_sigma_nm = %.3f\n', LER_left_sigma);   % Печатает LER левого края.
fprintf('LER_right_sigma_nm = %.3f\n', LER_right_sigma); % Печатает LER правого края.
fprintf('LER_avg_sigma_nm = %.3f\n', LER_avg_sigma);     % Печатает среднюю LER.
fprintf('LER_avg_3sigma_nm = %.3f\n', LER_avg_3sigma);   % Печатает среднюю LER в форме 3 sigma.
fprintf('NILS_avg = %.3f\n', NILS_avg);           % Печатает средний NILS.

% -----------------------------
% 9. Визуализация карты высоты, ширины, гистограммы и профиля интенсивности
% -----------------------------
figure;                                           % Создает новое окно для карты высоты.
imagesc(x, y, heightMap);                         % Отображает карту высоты в координатах x и y.
set(gca, 'YDir', 'normal');                       % Разворачивает ось y в привычное направление снизу вверх.
colorbar;                                         % Добавляет цветовую шкалу высоты.
hold on;                                          % Разрешает добавлять графики поверх карты высоты.
plot(leftEdges, yValid, 'w', 'LineWidth', 1.2);   % Рисует найденный левый край белой линией.
plot(rightEdges, yValid, 'w', 'LineWidth', 1.2);  % Рисует найденный правый край белой линией.
xlabel('x, nm');                                  % Подписывает ось x.
ylabel('y, nm');                                  % Подписывает ось y.
title('Synthetic resist height map and extracted edges'); % Добавляет заголовок карты высоты.

figure;                                           % Создает новое окно для ширины вдоль линии.
plot(yValid, widths, 'LineWidth', 1.2);           % Строит зависимость локальной ширины от координаты y.
grid on;                                          % Включает сетку на графике.
xlabel('y, nm');                                  % Подписывает ось y-координаты линии.
ylabel('line width, nm');                         % Подписывает ось ширины линии.
title('Line width along the structure');          % Добавляет заголовок графика ширины.

figure;                                           % Создает новое окно для гистограммы ширин.
histogram(widths, 30);                            % Строит гистограмму локальной ширины по 30 интервалам.
grid on;                                          % Включает сетку на гистограмме.
xlabel('line width, nm');                         % Подписывает ось значений ширины.
ylabel('count');                                  % Подписывает ось количества наблюдений.
title('Width distribution');                      % Добавляет заголовок гистограммы.

figure;                                           % Создает новое окно для профиля интенсивности.
plot(xI, intensity, 'LineWidth', 1.2);            % Строит модельный профиль интенсивности.
grid on;                                          % Включает сетку на графике интенсивности.
xlabel('x, nm');                                  % Подписывает ось x.
ylabel('normalized intensity');                   % Подписывает ось нормированной интенсивности.
title('Modeled intensity profile for NILS calculation'); % Добавляет заголовок графика интенсивности.
